package Switchcase;

public class Example2 {
	public static void main(String[] args) {
		
		String Month="xxx";
		switch(Month)
		{
		case "Jan":
			System.out.println("January");
			break;
		case "Feb":
			System.out.println("February");
			break;
		case "Mar":
			System.out.println("March");
			break;
		case "Apr":
			System.out.println("April");
		default:
			System.out.println("Wrong Month");
		}
	}

}
