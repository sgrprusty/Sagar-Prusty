package Logical_Program;

public class Multiplicationwithoutoperator 
{
	public static void main(String[] args) 
	{
		//Java program to multiple two numbers without using * operator
		
		int a=10;
		int b=5;
		int mult=0;
		
		for (int i=1;i<=b;i++)
		{
			mult=mult+a;
		}
		System.out.println(mult);
		
	}

}
